            <p>In February 2005, Francine began complaining of chest pains, little did we know that her days; begun a count-down without proper notification. The radiance she possessed never wavered, instead, she shone even more. In October 2005 after months of visitation to various doctors and numerous miss-diagnosis, it was discovered that this gentle angel had Thymic carcinoma, a rare type of cancer detected in persons 40-60 years old.</p>

            <p>A biopsy was ordered to be done to determine what steps needed to be taken to get her well. The biopsy was done and while in recovery, a conversation sparked between the two inseparable sisters when Francine made it known to Sweets to follow her dreams. Sweets made it clear that the only dream she has is to own a Funeral Home that would be able to cater to the needs and emotional grief of its clients when Francine jokingly said, “you can’t call it Sweets’ Funeral Home lol, I’d suggest something peaceful, something I’d want to comfortably go to, something “gardeny” or “heavenly”, then she laid back in her hospital bed, smiled and said...“Garden of Paradise Funeral Home”</p>

            <p>Francine sadly passed away after her one year battle with cancer on February 17, 2006 at the Montefiore Hospital in Bronx, New York but little did she know that her name would continually live on the lips of all who came in contact with her. </p>

            <p>Through her passing we have emerged ourselves into our work given the fact that we understand grief both personally and indirectly. Garden of Paradise Funeral Home was immediately dedicated to honor her memory with its guarantee to “grant relief in time of grief”.</p>

